    def display_menu(self, title, options):
        print(f"\n{title}")
        print("-" * 40)
        for i, option in enumerate(options, 1):
            print(f"{i}. {option}")
        print("-" * 40)

        while True:
            try:
                choice = int(input("请选择 (输入数字): "))
                if 1 <= choice <= len(options):
                    return options[choice - 1]
                else:
                    print(f"请输入1到{len(options)}之间的数字")
            except ValueError:
                print("请输入有效的数字")

    def get_numeric_input(self, prompt, min_val, max_val):
        while True:
            try:
                value = float(input(prompt))
                if min_val <= value <= max_val:
                    return value
                else:
                    print(f"请输入{min_val}到{max_val}之间的数值")
            except ValueError:
                print("请输入有效的数值")

    def calculate_risk(self):
        main_risk_types = list(self.risk_types.keys())
        main_risk_type = self.display_menu("请选择主风险类型", main_risk_types)

        sub_risk_types = self.risk_types[main_risk_type]
        sub_risk_type = self.display_menu("请选择具体风险类型", sub_risk_types)

        defaults = self.risk_defaults[sub_risk_type]
        L = defaults["L"]
        E = defaults["E"]
        C = defaults["C"]

        print(f"\n默认参数值:")
        print(f"事故发生的可能性 (L): {L}")
        print(f"人员暴露于危险环境的频率 (E): {E}")
        print(f"事故后果的严重程度 (C): {C}")

        time_period = self.display_menu("请选择时间段", self.time_periods)
        weather = self.display_menu("请选择天气条件", self.weathers)

        print("\n请评估防护措施的完备度 (0-10分，分值越高，措施越完善)")
        protection_scores = {}
        for measure, weight in self.protection_weights.items():
            score = self.get_numeric_input(f"{measure} (权重{weight}): ", 0, 10)
            protection_scores[measure] = score

        if time_period == "早高峰":
            Kt = 1.8
        elif time_period == "晚高峰":
            Kt = 1.5
        else:
            Kt = 1.0

        if weather in ["暴雨", "大雪", "雾霾"]:
            Kw = 1.5
        elif weather in ["阴天", "大风"]:
            Kw = 1.3
        else:
            Kw = 1.0

        K = Kt * Kw

        sum_WiSi = 0
        for measure, weight in self.protection_weights.items():
            sum_WiSi += weight * protection_scores[measure]

        V = 1 / (1 + sum_WiSi)

        D = L * E * C * K * V

        if D >= 320:
            risk_level = "一级 (极其危险)"
        elif D >= 160 and D <= 319:
            risk_level = "二级 (高度危险)"
        elif D >= 70 and D <= 159:
            risk_level = "三级 (显著危险)"
        elif D >= 20 and D <= 69:
            risk_level = "四级 (一般危险)"
        else:
            risk_level = "五级 (稍有危险)"

        suggestions = self.generate_suggestions(main_risk_type, sub_risk_type, risk_level)
        measures = self.generate_measures(main_risk_type, sub_risk_type, risk_level)

        self.display_result(D, risk_level, suggestions, measures)

